public class Ex03 {

	public static void main(String[] args) {
		// println() 매서드 - 출력		
		System.out.println("a");
		System.out.println("b");
		System.out.println("c");

		// print() 매서드 - 출력							줄바꿈(개행)의 여부 = CR '\r' + LF '\r'
		System.out.print("a"+'\n');
		System.out.print("b\r");
		System.out.print("c");
		System.out.println(); 		//개행용도
		System.out.print("c");
		

	}

}
